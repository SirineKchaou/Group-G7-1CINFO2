public class MedLarbiJouini {
    // Calculer la racine carrée d’un nombre
    public static double racineCarree(double n) {
        return Math.sqrt(n);
    }

    // Convertir une chaîne en majuscule
    public static String enMajuscule(String s) {
        return s.toUpperCase();
    }

    // Calculer la longueur d’une chaîne
    public static int longueurChaine(String s) {
        return s.length();
    }

}
